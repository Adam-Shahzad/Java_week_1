
public class Litrature {
	
int id;
String author;
String title;
int pg_number;
boolean is_issued;

public Litrature(int id, String author, String title, int pg_number, boolean is_issued) {
	super();
	this.id = id;
	this.author = author;
	this.title = title;
	this.pg_number = pg_number;
	this.is_issued = is_issued;
}

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getAuthor() {
	return author;
}
public void setAuthor(String author) {
	this.author = author;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public int getPg_number() {
	return pg_number;
}
public void setPg_number(int pg_number) {
	this.pg_number = pg_number;
}
public boolean isIs_issued() {
	return is_issued;
}
public void setIs_issued(boolean is_issued) {
	this.is_issued = is_issued;
}

public boolean checkID(int id){
	if (this.id == id){return true;}
	else return false;
}

@Override
public String toString() {
	
	return "Litrature [id=" + id + ", author=" + author + ", title=" + title + ", pg_number=" + pg_number
			+ ", is_issued=" + is_issued + "]";
}

}
